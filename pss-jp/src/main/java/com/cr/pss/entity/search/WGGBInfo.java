public class WGGBInfo {
	private String strAn;
	private String strPath;
	private String strPeriodicalNo;
	private long periodicalPage;
	private int intCurPage;

	public String getStrAn() {
		return strAn;
	}

	public void setStrAn(String strAn) {
		this.strAn = strAn;
	}

	public String getStrPath() {
		return strPath;
	}

	public void setStrPath(String strPath) {
		this.strPath = strPath;
	}

	public String getStrPeriodicalNo() {
		return strPeriodicalNo;
	}

	public void setStrPeriodicalNo(String strPeriodicalNo) {
		this.strPeriodicalNo = strPeriodicalNo;
	}

	public long getPeriodicalPage() {
		return periodicalPage;
	}

	public void setPeriodicalPage(long periodicalPage) {
		this.periodicalPage = periodicalPage;
	}

	public int getIntCurPage() {
		return intCurPage;
	}

	public void setIntCurPage(int intCurPage) {
		this.intCurPage = intCurPage;
	}
}
